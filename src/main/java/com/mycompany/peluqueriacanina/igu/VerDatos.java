    private void cargarTabla() {
        //DEFINIR EL MODELO QUE QUEREMOS QUE TENGA LA TABLA
        DefaultTableModel modeloTabla = new DefaultTableModel() {

            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        //NOMBRES COLUMNAS
        String titulos[] = {"Num", "Nombre", "Color", "Raza", "Alergico", "At. Esp.", "Nombre Dueño", "Cel."};
        modeloTabla.setColumnIdentifiers(titulos);

        //CARGA DATOS   
        List<Mascota> listaMascota = control.traetMascotas();

        //RECORRES LIST Y MOSTRAR ELEMENTOS EN LA TABLA
        if (listaMascota != null) {
            for (Mascota masco : listaMascota) {
                Object[] objeto = {masco.getNumCliente(), masco.getNombre(), masco.getColor(), masco.getRaza(),
                    masco.getAlergico(), masco.getAtenEsp(), masco.getUnDuenio().getNombre(), masco.getUnDuenio().getCelDuenio()};

                modeloTabla.addRow(objeto);
            }
        }

        tablaMascota.setModel(modeloTabla);
    }
