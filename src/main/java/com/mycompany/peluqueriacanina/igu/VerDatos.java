    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        //CONTROL DE TABLA NO VACIA Y TENGA SELECCIONADO ALGO
        if (tablaMascota.getRowCount() > 0) {
            if (tablaMascota.getSelectedRow() != -1) {
                int numCliente = Integer.parseInt(String.valueOf(tablaMascota.getValueAt(tablaMascota.getSelectedRow(), 0)));
                //LLAMO A BORRAR
                control.borrarMascota(numCliente);
                //LLAMO AL MSJ EXITOSO
                mostrarMensaje("Mascota eliminada correctamente", "info", "Borrado de Mascota");
                //VOLVER A CARGAR DATOS
                cargarTabla();
            } else {
                mostrarMensaje("No seleccionó ninguna Mascota", "error", "Error al eliminar");
            }
        } else {
            mostrarMensaje("No hay nada para eliminar en la tabla", "error", "Error al eliminar");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        //CONTROL DE TABLA NO VACIA Y TENGA SELECCIONADO ALGO
        if (tablaMascota.getRowCount() > 0) {
            if (tablaMascota.getSelectedRow() != -1) {
                int numCliente = Integer.parseInt(String.valueOf(tablaMascota.getValueAt(tablaMascota.getSelectedRow(), 0)));
                
                ModificarDatos pantallaModif = new ModificarDatos(numCliente);
                pantallaModif.setVisible(true);
                pantallaModif.setLocationRelativeTo(null);                
                
                this.dispose();
                //VOLVER A CARGAR DATOS
                cargarTabla();
            } else {
                mostrarMensaje("No seleccionó ninguna Mascota", "error", "Error al editar");
            }
        } else {
            mostrarMensaje("No hay nada para editar en la tabla", "error", "Error al editar");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

